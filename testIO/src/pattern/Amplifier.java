package pattern;
/**
 * 扩音器
 * 类与类之间的关系
 * 依赖：形参|局部变量
 * 关联：属性
 * 		聚合：属性 整体与部分 不一致的生命周期 人与手
 * 		组合：属性 整体与部分 一致的生命周期 人与大脑
 * 继承：父子类
 * 实现：接口和实现类
 * 
 * 
 * @author yinyiliang
 *
 */
public class Amplifier {

	private Voice voice;
	
	public Amplifier() {

	}

	public Amplifier(Voice voice) {
		super();
		this.voice = voice;
	}
	
	public void say(){
		System.out.println(voice.getVoice()*10000);
	}
}
